movements = {
  'R': [1, 0],
  'L': [-1, 0],
  'U': [0, 1],
  'D': [0, -1]
  }

def makeMove(headPos, tailPos, move) -> tuple[list, list]:
  headPos[0] += movements[move][0]
  headPos[1] += movements[move][1]
  dx = headPos[0] - tailPos[0]
  dy = headPos[1] - tailPos[1]
  if abs(dx) > 1 or abs(dy) > 1:
    if dx == 0: # vertical move
      tailPos[1] += dy // abs(dy) # move by  +/-1
    elif dy == 0: # horizontal move
      tailPos[0] += dx // abs(dx)
    else: # diagonal move
      tailPos[0] += dx // abs(dx)
      tailPos[1] += dy // abs(dy)

  return headPos, tailPos


def taskOne() -> None:
  aiHeadPos = [0, 0]
  aiTailPos = [0, 0]
  aoTailVisited = [aiTailPos.copy()]
  aoMovements = []

  with open('input', 'r') as fInputFile:
    while True:
        sLine = fInputFile.readline()
        if not sLine:
          break
        aoMovements.append([sLine.split()[0], int(sLine.split()[1])])

  for aMove in aoMovements:
    for i in range(aMove[1]):
      aiHeadPos, aiTailPos = makeMove(aiHeadPos, aiTailPos, aMove[0])
      if aiTailPos not in aoTailVisited:
        aoTailVisited.append(aiTailPos.copy())

  print("Tail visited {} positions.".format(str(len(aoTailVisited))))


