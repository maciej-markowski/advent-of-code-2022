def moveHead(headPos, move) -> list:
  headPos[0] += movements[move][0]
  headPos[1] += movements[move][1]
  return headPos


def moveTail(headPos, tailPos) -> list:
  dx = headPos[0] - tailPos[0]
  dy = headPos[1] - tailPos[1]
  if abs(dx) > 1 or abs(dy) > 1:
    if dx == 0: # vertical move
      tailPos[1] += dy // abs(dy) # move by  +/-1
    elif dy == 0: # horizontal move
      tailPos[0] += dx // abs(dx)
    else: # diagonal move
      tailPos[0] += dx // abs(dx)
      tailPos[1] += dy // abs(dy)
  return tailPos

def taskTwo() -> None:
  aoRope = {}
  for i in range(10):
    aoRope[i] = [0,0]
  aoTailVisited = [aoRope[9].copy()]
  aoMovements = []

  with open('input', 'r') as fInputFile:
    while True:
        sLine = fInputFile.readline()
        if not sLine:
          break
        aoMovements.append([sLine.split()[0], int(sLine.split()[1])])

  for aMove in aoMovements:
    for _ in range(aMove[1]):
      aoRope[0] = moveHead(aoRope[0], aMove[0])
      for i in range(1,10):
        aoRope[i] = moveTail(aoRope[i-1], aoRope[i])
      if aoRope[9] not in aoTailVisited:
        aoTailVisited.append(aoRope[9].copy())

  print("Tail of larger rope visited {} positions.".format(str(len(aoTailVisited))))



